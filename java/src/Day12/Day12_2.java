	// 전체 수강생 목록 출력 메소드 // ? : 와일드카드 => 모든 클래스 대응 
	public static void 수강생출력( 코스<?> course ) {
		System.out.println( course.get과정명()+" 수강생 : "+Arrays.toString( course.get회원목록() ) );
	}
	// 학생 목록 출력 메소드 // < ? extends 학생 > : 학생 클래스로부터 상속받은 모든 클래스 대응
	public static void 학생수강생출력( 코스< ? extends 학생 > course ) {
		System.out.println( course.get과정명()+" 수강생 : "+Arrays.toString( course.get회원목록() ) );
	}
	// 직장인수강생출력 메소드 // < ? super 직장인 > : 직장인 클래로부터 상속주는 모든 클래스 대응
	public static void 직장인수강생출력( 코스< ? super 직장인 > course ) {
		System.out.println( course.get과정명()+" 수강생 : "+Arrays.toString( course.get회원목록() ) );
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

