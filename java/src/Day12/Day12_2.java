		코스<학생> 학생코스 = new 코스<>("학생과정", 5);
			// 학생코스.추가( new 사람("유재석") ); 
			// 학생코스.추가( new 직장인("강호동") );	
			학생코스.추가( new 학생("신동엽"));
			학생코스.추가( new 고등학생("서장훈"));
		
		코스<고등학생> 고등학생코스 = new 코스<>("고등학생과정", 5 );
			// 고등학생코스.추가( new 사람("유재석") ); // 매개클래스 의 클래스이기 때문에 가능 
			// 고등학생코스.추가( new 직장인("강호동") );	// 매개클래스로 부터 상속받은 클래스도 가능 
			// 고등학생코스.추가( new 학생("신동엽"));
			고등학생코스.추가( new 고등학생("서장훈"));	
			
		///////////////////////// 코스 수강생 출력 ////////////////////////////////////
	
			수강생출력( 사람코스 );		// < ? > 모든 제네릭에 들어간 클래스 가능 
