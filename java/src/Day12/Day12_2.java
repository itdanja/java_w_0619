			사람코스.추가( new 학생("신동엽"));
			사람코스.추가( new 고등학생("서장훈"));

		코스<직장인> 직장인코스 = new 코스<>("직장인과정" , 5);
			// 직장인코스.추가( new 사람("유재석") ); // 서브의 매개클래스 인 경우 슈퍼클래스 못들어감
